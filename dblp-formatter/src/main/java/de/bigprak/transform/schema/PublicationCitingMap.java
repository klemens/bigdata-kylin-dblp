package de.bigprak.transform.schema;

public class PublicationCitingMap {
	private String publicationId;
	private String citedPublicationId;
	
	public String getPublicationId() {
		return publicationId;
	}
	public void setPublicationId(String publicationId) {
		this.publicationId = publicationId;
	}
	public String getCitedPublicationId() {
		return citedPublicationId;
	}
	public void setCitedPublicationId(String citedPublicationId) {
		this.citedPublicationId = citedPublicationId;
	}
	
}
