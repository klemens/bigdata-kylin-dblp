package de.bigprak.transform.schema;

public class Time {
	private int timeId;
	private int year;
	private int month;
	private int day;
	private int decade;
	
	public int getTimeId() {
		return timeId;
	}
	public void setTimeId(int timeId) {
		this.timeId = timeId;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getDecade() {
		return decade;
	}
	public void setDecade(int decade) {
		this.decade = decade;
	}
}
